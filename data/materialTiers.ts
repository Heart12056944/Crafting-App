export const materialTiers = Object.values(
  materialTierEntries.reduce<Record<number, { tier: number; label: string; names: string[] }>>((acc, entry) => {
    if (!acc[entry.tier]) acc[entry.tier] = { tier: entry.tier, label: entry.tierLabel, names: [] };
    acc[entry.tier].names.push(entry.name);
    return acc;
  }, {})
).sort((a, b) => a.tier - b.tier);

export function normalizeMaterialName(name: string): string {
  return name.trim().toLowerCase().replace(/\s+/g, " ");
}

export function getMaterialEntry(name: string): MaterialTierEntry | undefined {
  const normalized = normalizeMaterialName(name);
  return materialTierEntries.find((entry) => normalizeMaterialName(entry.name) === normalized);
}

export function getMaterialTier(name: string): number | undefined {
  return getMaterialEntry(name)?.tier;
}

export function getCraftingRarityForTier(tier: number): CraftingRarity | undefined {
  return tierToCraftingRarity[tier];
}

export function getMaterialCraftingRarity(name: string): CraftingRarity | undefined {
  return getMaterialEntry(name)?.rarity;
}

export function materialHasTag(name: string, tag: string): boolean {
  const entry = getMaterialEntry(name);
  return entry ? entry.tags.includes(tag.toLowerCase()) : false;
}

export function getMaterialsByTag(tag: string): MaterialTierEntry[] {
  const normalized = tag.toLowerCase();
  return materialTierEntries.filter((entry) => entry.tags.includes(normalized));
}

export function getPhaseTouchedCounterpart(baseMaterialName: string): string {
  if (baseMaterialName.toLowerCase().includes("phase")) return baseMaterialName;
  return `Phase-Touched ${baseMaterialName}`;
}

